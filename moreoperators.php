<h2>Ternary operator</h2>
De Ternary operator heeft zijn naam omdat het 3 eigenschappen nodig heeft: een stelling, een uitkomst voor true, en een uitkomst voor false.
De ternary operator wordt voornamelijk gebruikt bij logische structuren, en wordt vaak als vervanger gebruikt voor de if-else statement.

<h2>Dot operator</h2>
De dot operator ('.'), zorgt voor de aaneenschakeling van de rechter en linker argumenten.

<h2>++ en --</h2>
De ++ operator verhoogd de waarde van zijn operand met +1. De operand moet een rekenkundig of een wijzer gegevens type, en
hij moet verwijzen naar een bewerkbaar data object.
De -- operator werkt bijna hetzelfde als de ++ operator het enige verschil is dat de -- operator de waarde van zijn operand veranderdt met -1.

<h2> +=, -=, *=, /= en .=</h2>
Deze operators zorgen ervoor dat de waarden die ze hadden worden berekend met de waarden die erachter staan. welke som eruit komt heeft te maken of je += of een andere gebruikt.
